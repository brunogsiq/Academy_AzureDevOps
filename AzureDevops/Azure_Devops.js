/*
    1) Issues:
        Necessário para problemas de desenvolvimento, mesmo que o Q.A. tenha resolvido junto ao setor de Desenvolvimento.

        Como e onde criar?
            Boards - Sprints - New - Issue

        Classificação deverá ser feito de acordo com o motivo, para que as métricas sejam feitas correamente.

        Para que uma issue fica vinculaa de forma correta a uma User story ou PBI, deverá ser feita da seguinte maneira:
            US/PBI > Add lInk > New Item:
                Link type = Parent
                Work item type: Issue
                Title= Título
                Após item aberto, desvincula o mesmo como child e vincula a US/PBI como Parent, assim
                    poderá ser observada no board na mesma linha hierárquica.

        A issue aberta terá as seguintes informações
            1)Teste realizado e comportamento
            2)Regra de negócio
            3)Evidenciamento do erro/falha
            4)Sugestão de correção

    2) User Story:
        Ajudam na compreensão do que deverá ser entregue.
        Usadas também para criação de critérios de aceitação que irão definir quando uma determinada funcionalidade estará pronta.
        Contribuem para a garantia de que as necessidades do usuário sejam levadas em consideração em todas as etapas do processo de desenvolvimento,
            consequentemente atendendo as expectativas do Cliente, trazendo assim maior qualidade ao produto desenvolvido.

        A U.S. deverá estar estruturada da seguinte forma:
            Epic > Feature > User Story

        Criando a U.S.:
            Crie uma Feature, em Related Work, vá em Add link > New Item.
            Vincule uma U.S.
            Em link type, escolha Child.
            Em work item type, escolha U.S.
            Após é somente dar seguimento, inserindo tags, título, entre outras possíveis configurações.

        Formato de U.S.:
            A mesma deverá ser construída no formato Gherkin, ou seja, Como > Quero > Para.
            Lembre-se de refinar, detalhar, quanto mais detalhes, melhor a construção da funcionalidade.

        Atente-se que uma U.S. bacana terá as seguintes caracterísitcas:
            Independente, Negociável, Valiosa, Estimável, Pequena, Testável.

        A U.S. também deverá ser composta dos critérios de aceite, que trata-se de uma lista de requisitos que deverão ser atendidos 
            para que a uma determinada funcionalidade seja considerada concluída.

    3) Original estimate:
        Trate-se da projeção de tempo que uma determinada tarefa ou trabalho deverá ser concluída, isso inclui tarefas de teste.
        Esta deverá ser preenchida até a Sprint Planning.

    4) Q.A. estimate:
        Trate-se da projeção de tempo que uma determinada tarefa deverá ser concluída no âmbito de testes, ou revisão,
            Atenção, o tempo que foi estimado deverá ser adicionado a original estimate.
            O mesmo também deverá ser preenchido até a Sprint Planning.

    5) DoD, Definition of Done:
        Segue abaixo exemplo.

        Testes unitários variados.
        Testes funcionais aprovados.
        Critérios de aceite contemplados.
        Códigos revisados.
        Testes realizados.
        P.O concorda com o realizado.

    6) Expedite:
        Servirá para identificação de itens de alta prioridade e atenção imediata, destacando itens critícos.
        Para cumprimento da Sprint, recomenda-se apenas um item com classificação expedite.

    7) Hierarquia:
        User Stoiry ou PBI > Task (Parent) > Issue (Parent)]
            Bugs serão abertor na mesma hierarquia de uma PBI ou User Story, ou seja,
                Deverá ser aberto uma issue caso queira vincular a US ou PBI

*/